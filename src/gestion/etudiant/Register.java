    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            if (jTextField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nom obligatoire","Attention",  JOptionPane.WARNING_MESSAGE);
            } else if (jTextField2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pr√©nom obligatoire","Attention",  JOptionPane.WARNING_MESSAGE);
            } else if (jTextField3.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Email obligatoire","Attention",  JOptionPane.WARNING_MESSAGE);
            } else if (jPasswordField1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Mot de passe obligatoire","Attention",  JOptionPane.WARNING_MESSAGE);
            } else if (jPasswordField2.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Confirmer mot de passe obligatoire","Attention",  JOptionPane.WARNING_MESSAGE);
            }
            else if (!jPasswordField1.getText().equalsIgnoreCase(jPasswordField2.getText())) {
                JOptionPane.showMessageDialog(null, "Les mots de passe ne sont pas identique","Attention",  JOptionPane.WARNING_MESSAGE);
            }
            else {

                ConnectionDB connectionDB = new ConnectionDB();
                String sql = "insert into etudiant(nom,prenom,email,password)  values(?,?,?,?)";
                PreparedStatement statement = connectionDB.getConnection().prepareStatement(sql);
                statement.setString(1, jTextField1.getText());
                statement.setString(2, jTextField2.getText());
                statement.setString(3, jTextField3.getText());
                statement.setString(4, jPasswordField1.getText());
                statement.executeUpdate();
                Authentification authentification = new Authentification();
                authentification.setVisible(true);
                this.setVisible(false);

            }
        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

