    private void loginEtudiant() {
       
         
        try {
             ConnectionDB connectionDB = new ConnectionDB();
             String sql = "select * from etudiant where email=? and password= ?";
             PreparedStatement statement = connectionDB.getConnection().prepareStatement(sql);
             statement.setString(1, jTextField1.getText());
             statement.setString(2, jPasswordField1.getText());
             ResultSet res = statement.executeQuery();
             if (res.next()) {
                 idEtudiant = res.getInt(1);
                 HomeEtudiant home = new HomeEtudiant();
                 home.setVisible(true);
                 this.setVisible(false);
             } else {
                  JOptionPane.showMessageDialog(null, "Merci de verifier votre email ou mot de passe","Attention",  JOptionPane.WARNING_MESSAGE);
             }
        } catch (SQLException ex) {
            Logger.getLogger(Authentification.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

